package com.github.fedorchuck.developers_notification.json.model;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;
import java.util.Objects;

/**
 * <p> <b>Author</b>: <a href="http://vl-fedorchuck.rhcloud.com/">Volodymyr Fedorchuk</a> </p>
 * @author <a href="http://vl-fedorchuck.rhcloud.com/">Volodymyr Fedorchuk</a>
 * @since 0.1.0
 */
public class Payload implements Serializable {
    private String username;
    private String icon_url;
    private String text;
    private String channel;
    private List<Attachment> attachments = new ArrayList<Attachment>(0);

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getIcon_url() {
        return icon_url;
    }

    public void setIcon_url(String icon_url) {
        this.icon_url = icon_url;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getChannel() {
        return channel;
    }

    public void setChannel(String channel) {
        this.channel = channel;
    }

    public List<Attachment> getAttachments() {
        return attachments;
    }

    public void setAttachments(List<Attachment> attachments) {
        this.attachments = attachments;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Payload payload = (Payload) o;
        return Objects.equals(username, payload.username) &&
                Objects.equals(icon_url, payload.icon_url) &&
                Objects.equals(text, payload.text) &&
                Objects.equals(channel, payload.channel) &&
                Objects.equals(attachments, payload.attachments);
    }

    @Override
    public int hashCode() {

        return Objects.hash(username, icon_url, text, channel, attachments);
    }
}
